    public void carregarTabelaLivros() {
        try {
            List<Livro> resultado = livroDAO.consutarTodosLivros();

            livroTableModel.setLivros(resultado);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao carregar dados dos livros");
            e.printStackTrace();
        }
    }

    public void buscarPorFiltro() {
        String filtroSelecionado = cbFiltro.getSelectedItem().toString();
        String filtroValor = txtFiltro.getText();

        Integer id = null;
        String titulo = null;
        String autor = null;
        String isbn = null;
        String editora = null;
        LocalDate dataPublicacao = null;
        String classificacao = null;

        switch (filtroSelecionado) {
            case "ID":
                id = Integer.parseInt(filtroValor);
                break;
            case "Título":
                titulo = filtroValor;
                break;
            case "Autor":
                autor = filtroValor;
                break;
            case "ISBN":
                isbn = filtroValor;
                break;
            case "Editora":
                editora = filtroValor;
                break;
            case "Data Pub.":    
                try {
                DateTimeFormatter format = DateTimeFormatter.ofPattern("dd/MM/yyyy");
                dataPublicacao = LocalDate.parse(filtroValor, format);
                } catch (Exception e) {
                    JOptionPane.showConfirmDialog(this, "Formato de data inválido");
                    return;
                }
                break;
            case "Classificação":
                if(!filtroValor.isEmpty()) classificacao = filtroValor;
                break;
            default:
                JOptionPane.showMessageDialog(this, "Não foi "
                        + "possível aplicar filtro");
                return;
        }
        
        List<Livro> livroFiltro = livroDAO.consultarLivro(id, titulo, autor, isbn, 
                editora,dataPublicacao, classificacao);
        
        livroTableModel.setLivros(livroFiltro);
    }

